class Main {  
    public static void main(String args[]) {  
         Console.println("Hello Java");
         for (int i = 0; i < 200; i++) {
             if (i > 0)
                 Console.println("i>0");
             if (i > 100)
                 Console.println("i>100");
         }
         for (long i = 0; i < 200; i++) {
             if (i > 0)
                 Console.println("l>0");
             if (i > 100)
                 Console.println("l>100");
         }
    }
}    
