class Main {  
    public static void main(String args[], int ii, long ii2) {  
         Console.println("Hello Java");
         for (int i = ii; i < 200; i++) {
             if (i > 0)
                 Console.println("i>0");
             if (i > 100)
                 Console.println("i>100");
         }
         for (long i = ii2; i < 200; i++) {
             if (i > 0)
                 Console.println("l>0");
             if (i > 100)
                 Console.println("l>100");
         }
    }
}    
